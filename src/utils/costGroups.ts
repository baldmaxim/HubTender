/**
 * Общие определения над-групп категорий затрат.
 *
 * ОТДЕЛОЧНЫЕ РАБОТЫ — это одна запись cost_categories. А «ВНУТРЕННИЕ ИНЖЕНЕРНЫЕ
 * СИСТЕМЫ» — синтетическая над-группа, объединяющая несколько отдельных
 * cost_categories, чьи имена начинаются с «ВИС /». В БД родителя/группы нет —
 * группировка целиком фронтовая (по cost_categories.name). Модуль держит это
 * определение в одном месте, чтобы страницы «Затраты на строительство» и
 * «Сравнение затрат» не разъезжались.
 */

/** Отображаемое имя над-группы в таблицах и Excel. */
export const VIS_SUPER_GROUP_NAME = 'ВНУТРЕННИЕ ИНЖЕНЕРНЫЕ СИСТЕМЫ';

/**
 * Общий ключ для construction_cost_volumes.group_key. Один и тот же на обеих
 * страницах, поэтому объём/примечание, введённые в «Затратах на строительство»,
 * видны в «Сравнении» (read-only) и дают одинаковый пересчёт «…/ед.».
 */
export const VIS_SUPER_GROUP_KEY = 'group-ВНУТРЕННИЕ ИНЖЕНЕРНЫЕ СИСТЕМЫ';

/**
 * Признак ВИС-категории по префиксу имени. По аналогии с .includes('отделочн')
 * у ОТДЕЛОЧНЫХ РАБОТ — устойчиво к точному написанию суффикса. Ловит варианты
 * «ВИС / …» и «ВИС/…».
 */
export function isVisCategory(name: string): boolean {
  return /^вис\s*\//.test((name || '').trim().toLowerCase());
}
