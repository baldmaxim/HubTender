#!/usr/bin/env node
// 11_repair_yandex_tenders_updated_at — targeted repair of ONLY
// public.tenders.updated_at on the Yandex target, restoring it from the PROD
// export NDJSON snapshot (grand-total recalc triggers re-stamped it during the
// import). No other table, no other column, no PROD DB read.
//
// =============================== SAFETY ====================================
//  - Target = YANDEX_DATABASE_URL only (verify-full). PROD DB NOT read; OLD
//    forbidden (assertNoOldEnv, exit 7). NEVER prints DSN/secrets.
//  - Default: NOTHING changed. --dry-run plans only.
//  - --apply additionally REQUIRES env ALLOW_REPAIR_YANDEX_DATA=true.
//  - Only `UPDATE public.tenders SET updated_at = <exported> WHERE id = <id>`.
//    No other column/table. No DROP/TRUNCATE. No session_replication_role.
//  - Refuses if ANY non-updated_at business column differs PROD-export↔Yandex.
//  - Temporarily disables ONLY the public.tenders handle_updated_at user
//    trigger (exact name, tgisinternal=false); re-enabled in finally.
//  - Exit codes: 0 ok · 2 config/precondition · 3 blocker · 7 guard · 1 fail.
// ===========================================================================

import { existsSync, readFileSync, writeFileSync, mkdirSync, createReadStream } from 'node:fs';
import { join } from 'node:path';
import { createInterface } from 'node:readline';
import {
  loadEnv, env, requireEnv, getExportDir, resolveCa, getYandexClient,
  tag, parseCliArgs, fatal, assertNoOldEnv, maskHost, DOC_DIR,
} from './_lib.mjs';

loadEnv();

const REPORT = join(DOC_DIR, '17_TENDERS_UPDATED_AT_REPAIR_RESULT.md');
const STATUS = {
  DRY: 'TENDERS_UPDATED_AT_REPAIR_DRY_RUN_OK',
  OK: 'TENDERS_UPDATED_AT_REPAIR_OK',
  FAILED: 'TENDERS_UPDATED_AT_REPAIR_FAILED',
};

const { values } = parseCliArgs({
  name: '11_repair_yandex_tenders_updated_at.mjs',
  description: 'Restore ONLY public.tenders.updated_at on Yandex from the PROD export NDJSON.',
  options: {
    'dry-run': { type: 'boolean', default: false, describe: 'Plan only; no changes (also the default)' },
    apply:     { type: 'boolean', default: false, describe: 'Execute repair (requires ALLOW_REPAIR_YANDEX_DATA=true)' },
    'export-dir': { type: 'string', default: '', describe: 'Override EXPORT_DIR env' },
  },
});
const doApply = values.apply === true;
const dryRun = !doApply;
const exportDir = values['export-dir'] || env('EXPORT_DIR') || getExportDir();

function s(v) { return v === null || v === undefined ? null : String(v); }

function writeReport(r) {
  mkdirSync(DOC_DIR, { recursive: true });
  const L = [];
  L.push('# 17. TENDERS.updated_at REPAIR RESULT');
  L.push('');
  L.push('> Generated by `scripts/prod-to-yandex/11_repair_yandex_tenders_updated_at.mjs`.');
  L.push('> Overwritten on every run. Target = Yandex only; PROD DB not read.');
  L.push('> Rationale: grand-total recalc triggers re-stamped tenders.updated_at');
  L.push('> during import; only the audit timestamp differs (data is correct).');
  L.push('');
  L.push(`- Run (UTC): ${r.ts}`);
  L.push(`- Mode: **${r.mode}**`);
  L.push(`- Target host: ${r.host}`);
  L.push(`- Export snapshot: ${r.ndjson}`);
  L.push('');
  L.push('## Preconditions');
  L.push('');
  for (const p of r.preconds) L.push(`- ${p.ok ? '✓' : '✗'} ${p.name}${p.detail ? ` — ${p.detail}` : ''}`);
  L.push('');
  L.push('## Diff analysis (PROD export ↔ Yandex)');
  L.push('');
  L.push(`- tenders total: export=${r.exportCount} yandex=${r.yandexCount}`);
  L.push(`- id sets equal: ${r.idsEqual ? '✓' : '✗'} (only-export=${r.onlyExport}, only-yandex=${r.onlyYandex})`);
  L.push(`- rows with updated_at mismatch: ${r.updatedAtMismatch}`);
  L.push(`- non-updated_at business columns differing: ${r.otherColsDiff.length ? r.otherColsDiff.join(', ') : '(none — safe)'}`);
  if (r.sampleIds.length) L.push(`- sample mismatched ids (first ${r.sampleIds.length}): ${r.sampleIds.join(', ')}`);
  L.push('');
  L.push('## Action');
  L.push('');
  L.push(`- rows that ${r.applied ? 'were' : 'would be'} updated (updated_at only): ${r.toUpdate}`);
  L.push(`- trigger temporarily disabled: ${r.trigger ? `public.tenders.${r.trigger}` : '(none found)'}`);
  L.push(`- trigger re-enabled: ${r.reenabled ? '✓' : (r.applied ? '✗' : 'n/a (dry-run)')}`);
  if (r.applied) {
    L.push(`- post-apply updated_at mismatch: ${r.postMismatch}`);
    L.push(`- post-apply business-column drift: ${r.postOtherDiff != null ? r.postOtherDiff : 'n/a'}`);
  }
  L.push('');
  if (r.errors.length) { L.push('## Errors'); L.push(''); r.errors.forEach((e) => L.push(`- ❌ ${e}`)); L.push(''); }
  L.push('## Final status');
  L.push('');
  L.push('```');
  L.push(r.status);
  L.push('```');
  L.push('');
  try { writeFileSync(REPORT, L.join('\n'), 'utf8'); console.log(`✓ wrote ${REPORT}`); }
  catch (e) { console.error(`✗ failed to write ${REPORT}: ${e.message}`); }
}

async function main() {
  try { assertNoOldEnv(); } catch (e) { console.error(`✗ ${e.message}`); process.exit(7); }

  const r = {
    ts: new Date().toISOString(),
    mode: dryRun ? 'dry-run' : 'apply',
    host: '(unknown)', ndjson: '', preconds: [],
    exportCount: 0, yandexCount: 0, idsEqual: false, onlyExport: 0, onlyYandex: 0,
    updatedAtMismatch: 0, otherColsDiff: [], sampleIds: [], toUpdate: 0,
    trigger: null, reenabled: false, applied: false,
    postMismatch: null, postOtherDiff: null, errors: [], status: STATUS.FAILED,
  };
  const addP = (name, ok, detail) => r.preconds.push({ name, ok: !!ok, detail: detail || '' });

  if (doApply && env('ALLOW_REPAIR_YANDEX_DATA') !== 'true') {
    r.errors.push('Real repair refused: ALLOW_REPAIR_YANDEX_DATA != true.');
    writeReport(r);
    console.error('✗ --apply requires ALLOW_REPAIR_YANDEX_DATA=true. Use --dry-run to plan.');
    process.exit(7);
  }

  // ---- Export snapshot (manifest source_label must be PROD_SUPABASE) ----
  const manifestPath = join(exportDir, 'manifest.json');
  if (!existsSync(manifestPath)) {
    r.errors.push(`manifest.json not found in ${exportDir}`);
    writeReport(r); console.error(`✗ manifest.json not found in ${exportDir}`); process.exit(2);
  }
  const manifest = JSON.parse(readFileSync(manifestPath, 'utf8'));
  addP('manifest.source_label = PROD_SUPABASE', manifest.source_label === 'PROD_SUPABASE', manifest.source_label);
  if (manifest.source_label !== 'PROD_SUPABASE') {
    r.errors.push(`manifest.source_label=${manifest.source_label}; expected PROD_SUPABASE`);
    writeReport(r); console.error('✗ wrong manifest.source_label'); process.exit(2);
  }
  const tEntry = (manifest.tables || []).find((t) => t.schema === 'public' && t.table === 'tenders');
  r.ndjson = (tEntry && tEntry.ndjson_path) ? tEntry.ndjson_path : 'data/public.tenders.ndjson';
  const ndjsonAbs = join(exportDir, r.ndjson);
  if (!existsSync(ndjsonAbs)) {
    r.errors.push(`tenders NDJSON not found: ${r.ndjson}`);
    writeReport(r); console.error(`✗ ${r.ndjson} not found`); process.exit(2);
  }
  // Yandex schema verify doc must exist (status recorded, not gated — post-
  // import it is legitimately FAILED on non-empty rows).
  const sv = join(DOC_DIR, '09_SCHEMA_VERIFY_RESULT.md');
  addP('09_SCHEMA_VERIFY_RESULT.md present', existsSync(sv),
    existsSync(sv) ? (readFileSync(sv, 'utf8').match(/SCHEMA_VERIFY_(OK_WITH_WARNINGS|OK|FAILED)/) || ['(none)'])[0] : 'missing');

  const yaUrl = (() => { try { return requireEnv('YANDEX_DATABASE_URL'); }
    catch (e) { r.errors.push(e.message); writeReport(r); console.error(`✗ ${e.message}`); process.exit(2); } })();
  r.host = maskHost(yaUrl);
  const ca = resolveCa();
  if (!ca.ok) { r.errors.push(`Yandex CA unavailable (${ca.reason})`); writeReport(r); console.error(`✗ Yandex CA unavailable: ${ca.reason}`); process.exit(2); }

  // Load PROD export tenders snapshot (raw text values, as exported).
  const exp = new Map();
  for await (const line of createInterface({ input: createReadStream(ndjsonAbs) })) {
    if (!line.trim()) continue;
    const row = JSON.parse(line);
    exp.set(row.id, row);
  }
  r.exportCount = exp.size;

  const client = await getYandexClient(yaUrl, ca.pem, { applicationName: 'prod-to-yandex-repair-tenders-uat' });
  try {
    const tReg = (await client.query("SELECT to_regclass('public.tenders') AS r")).rows[0].r;
    addP('public.tenders exists', tReg != null);
    if (tReg == null) { r.errors.push('public.tenders missing'); writeReport(r); console.error('✗ public.tenders missing'); await client.end().catch(()=>{}); process.exit(3); }

    const { rows: yaRows } = await client.query('SELECT * FROM public.tenders');
    r.yandexCount = yaRows.length;
    const yMap = new Map(yaRows.map((x) => [x.id, x]));
    addP('row count equal (export == yandex)', r.exportCount === r.yandexCount,
      `export=${r.exportCount} yandex=${r.yandexCount}`);

    let onlyExport = 0, onlyYandex = 0;
    for (const id of exp.keys()) if (!yMap.has(id)) onlyExport++;
    for (const id of yMap.keys()) if (!exp.has(id)) onlyYandex++;
    r.onlyExport = onlyExport; r.onlyYandex = onlyYandex;
    r.idsEqual = onlyExport === 0 && onlyYandex === 0;
    addP('id set equal', r.idsEqual, `only-export=${onlyExport} only-yandex=${onlyYandex}`);

    // Per-column diff (id-matched). Refuse if any non-updated_at col differs.
    const cols = r.exportCount ? Object.keys([...exp.values()][0]) : [];
    const otherDiff = new Set();
    const mismatchIds = [];
    for (const [id, e] of exp) {
      const y = yMap.get(id);
      if (!y) continue;
      for (const c of cols) {
        if (s(e[c]) !== s(y[c])) {
          if (c === 'updated_at') { if (!mismatchIds.includes(id)) mismatchIds.push(id); }
          else otherDiff.add(c);
        }
      }
    }
    r.updatedAtMismatch = mismatchIds.length;
    r.otherColsDiff = [...otherDiff];
    r.sampleIds = mismatchIds.slice(0, 5);
    addP('only updated_at differs (no business-column drift)', otherDiff.size === 0,
      otherDiff.size ? `differing: ${[...otherDiff].join(', ')}` : 'safe');

    const allOk = r.preconds.every((p) => p.ok);
    if (!allOk) {
      r.errors.push('precondition failed — refusing to repair (read-only comparison stopped).');
      r.status = STATUS.FAILED;
      writeReport(r);
      console.error('✗ TENDERS_UPDATED_AT_REPAIR_FAILED — precondition(s) failed; nothing changed.');
      await client.end().catch(() => {});
      process.exit(3);
    }

    r.toUpdate = r.updatedAtMismatch;
    console.log(`${tag('YA')} tenders=${r.yandexCount} · updated_at mismatch=${r.updatedAtMismatch} · other-col drift=${r.otherColsDiff.length}`);

    if (dryRun) {
      r.status = STATUS.DRY;
      writeReport(r);
      console.log(`${tag('YA')} would UPDATE only public.tenders.updated_at on ${r.toUpdate} row(s); no other column/table.`);
      console.log(`${tag('YA')} status: ${STATUS.DRY} (nothing written to Yandex)`);
      await client.end().catch(() => {});
      return;
    }

    // ---- APPLY: disable tenders handle_updated_at trigger, fix updated_at ----
    const { rows: trg } = await client.query(`
      SELECT t.tgname FROM pg_trigger t
        JOIN pg_class c ON c.oid=t.tgrelid
        JOIN pg_namespace n ON n.oid=c.relnamespace
        JOIN pg_proc p ON p.oid=t.tgfoid
       WHERE NOT t.tgisinternal AND n.nspname='public' AND c.relname='tenders'
         AND p.proname='handle_updated_at'`);
    const trigName = trg.length && /^[a-zA-Z_][a-zA-Z0-9_]*$/.test(trg[0].tgname) ? trg[0].tgname : null;
    r.trigger = trigName;

    if (trigName) await client.query(`ALTER TABLE public.tenders DISABLE TRIGGER "${trigName}"`);
    try {
      await client.query('BEGIN');
      let updated = 0;
      for (const [id, e] of exp) {
        const res = await client.query(
          'UPDATE public.tenders SET updated_at = $1 WHERE id = $2 AND updated_at IS DISTINCT FROM $1',
          [e.updated_at, id],
        );
        updated += res.rowCount ?? 0;
      }
      await client.query('COMMIT');
      r.applied = true;
      r.toUpdate = updated;
      console.log(`${tag('YA')} updated_at restored on ${updated} tenders row(s)`);
    } catch (e) {
      try { await client.query('ROLLBACK'); } catch { /* ignore */ }
      throw e;
    } finally {
      if (trigName) {
        try {
          await client.query(`ALTER TABLE public.tenders ENABLE TRIGGER "${trigName}"`);
          r.reenabled = true;
          console.log(`${tag('YA')} re-enabled trigger public.tenders.${trigName}`);
        } catch (e) {
          r.errors.push(`FAILED to re-enable public.tenders.${trigName}: ${String(e?.message || e)}`);
          console.error(`✗ re-enable manually: ALTER TABLE public.tenders ENABLE TRIGGER "${trigName}";`);
        }
      }
    }

    // ---- Post-apply verification ----
    const { rows: ya2 } = await client.query('SELECT * FROM public.tenders');
    const y2 = new Map(ya2.map((x) => [x.id, x]));
    let postMis = 0, postOther = 0;
    for (const [id, e] of exp) {
      const y = y2.get(id);
      if (!y) continue;
      for (const c of cols) {
        if (s(e[c]) !== s(y[c])) { if (c === 'updated_at') postMis++; else postOther++; }
      }
    }
    r.postMismatch = postMis; r.postOtherDiff = postOther;
    if (postMis === 0 && postOther === 0 && r.reenabled) {
      r.status = STATUS.OK;
      console.log(`${tag('YA')} ✓ post-apply: updated_at mismatch=0, no business drift, trigger re-enabled`);
    } else {
      r.status = STATUS.FAILED;
      r.errors.push(`post-apply check failed: updated_at_mismatch=${postMis} other_drift=${postOther} reenabled=${r.reenabled}`);
    }
    writeReport(r);
    console.log(`${tag('YA')} status: ${r.status}`);
    if (r.status === STATUS.FAILED) { await client.end().catch(() => {}); process.exit(1); }
  } catch (e) {
    r.status = STATUS.FAILED;
    r.errors.push(String(e?.message || e).replace(/postgres(?:ql)?:\/\/\S+/gi, '<redacted-conn>'));
    writeReport(r);
    fatal(e);
  } finally {
    await client.end().catch(() => {});
  }
}

main().catch((e) => fatal(e));
