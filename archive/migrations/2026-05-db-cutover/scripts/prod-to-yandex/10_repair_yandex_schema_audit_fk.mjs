#!/usr/bin/env node
// 10_repair_yandex_schema_audit_fk — controlled repair of the ALREADY-APPLIED
// Yandex schema: drop the spurious enforced FK on
// public.boq_items_audit.boq_item_id → public.boq_items and ensure the
// supporting (non-FK) lookup index exists.
//
// =============================== SAFETY ====================================
//  - Target = YANDEX_DATABASE_URL only (verify-full). OLD never used.
//  - NEVER prints DSN / passwords / secrets.
//  - Default: NOTHING is changed. --dry-run plans only.
//  - --apply additionally REQUIRES env ALLOW_REPAIR_YANDEX_SCHEMA=true.
//  - Only DROP CONSTRAINT on the audit-FK + CREATE INDEX IF NOT EXISTS.
//    No PK drop, no other FK touched, no business data changed.
//    No session_replication_role. No CREATE EXTENSION/ROLE.
//  - Exit codes: 0 ok · 2 config/precondition · 7 guard · 1 failed.
// ===========================================================================

import { writeFileSync, mkdirSync } from 'node:fs';
import { join } from 'node:path';
import {
  loadEnv, env, requireEnv, resolveCa, getYandexClient, tag,
  parseCliArgs, fatal, assertNoOldEnv, maskHost, DOC_DIR,
} from './_lib.mjs';

loadEnv();

const REPORT = join(DOC_DIR, '16_SCHEMA_REPAIR_AUDIT_FK_RESULT.md');
const INDEX_NAME = 'idx_boq_items_audit_boq_item_id';
const STATUS = {
  DRY: 'SCHEMA_REPAIR_DRY_RUN_OK',
  OK: 'SCHEMA_REPAIR_OK',
  FAILED: 'SCHEMA_REPAIR_FAILED',
};

const { values } = parseCliArgs({
  name: '10_repair_yandex_schema_audit_fk.mjs',
  description: 'Drop spurious boq_items_audit.boq_item_id FK + ensure lookup index (Yandex).',
  options: {
    'dry-run': { type: 'boolean', default: false, describe: 'Plan only; no changes (also the default)' },
    apply:     { type: 'boolean', default: false, describe: 'Execute repair (requires ALLOW_REPAIR_YANDEX_SCHEMA=true)' },
  },
});
const doApply = values.apply === true;
const dryRun = !doApply; // default and --dry-run both mean "no changes"

// Any FK on public.boq_items_audit whose column set includes boq_item_id and
// which references public.boq_items (covers the canonical name and any rename).
const FIND_FK_SQL = `
  SELECT con.conname
    FROM pg_constraint con
    JOIN pg_class c  ON c.oid  = con.conrelid
    JOIN pg_namespace n ON n.oid = c.relnamespace
    JOIN pg_class rc ON rc.oid = con.confrelid
   WHERE n.nspname='public' AND c.relname='boq_items_audit'
     AND con.contype='f' AND rc.relname='boq_items'
     AND 'boq_item_id' = ANY (
           SELECT a.attname FROM unnest(con.conkey) k
             JOIN pg_attribute a ON a.attrelid=con.conrelid AND a.attnum=k)`;
const FIND_IDX_SQL =
  `SELECT 1 FROM pg_indexes WHERE schemaname='public' AND tablename='boq_items_audit' AND indexname=$1`;

function isIdent(s) { return /^[a-zA-Z_][a-zA-Z0-9_]*$/.test(s); }

function writeReport(r) {
  mkdirSync(DOC_DIR, { recursive: true });
  const L = [];
  L.push('# 16. SCHEMA REPAIR — audit FK (boq_items_audit.boq_item_id)');
  L.push('');
  L.push('> Generated by `scripts/prod-to-yandex/10_repair_yandex_schema_audit_fk.mjs`.');
  L.push('> Overwritten on every run. Target = Yandex only. No DSN printed.');
  L.push('> Rationale: docs/yandex-migration/15_AUDIT_FK_SCHEMA_DECISION.md.');
  L.push('');
  L.push(`- Run (UTC): ${r.ts}`);
  L.push(`- Mode: **${r.mode}**`);
  L.push(`- Target host: ${r.host}`);
  L.push('');
  L.push('## Detected');
  L.push('');
  L.push(`- FK(s) on boq_items_audit.boq_item_id → boq_items: ${r.fks.length ? r.fks.join(', ') : '(none)'}`);
  L.push(`- index \`${INDEX_NAME}\`: ${r.indexBefore ? 'present' : 'absent'}`);
  L.push('');
  L.push('## Planned / executed');
  L.push('');
  if (r.fks.length) for (const f of r.fks) L.push(`- ${r.applied ? 'EXECUTED' : 'PLANNED'}: \`ALTER TABLE public.boq_items_audit DROP CONSTRAINT ${f};\``);
  else L.push('- no FK to drop (already audit-compatible)');
  L.push(`- ${r.applied ? 'EXECUTED' : 'PLANNED'}: \`CREATE INDEX IF NOT EXISTS ${INDEX_NAME} ON public.boq_items_audit(boq_item_id);\``);
  L.push('');
  if (r.applied) {
    L.push('## Post-apply verification');
    L.push('');
    L.push(`- FK absent: ${r.fkAbsentAfter ? '✓' : '✗'}`);
    L.push(`- index \`${INDEX_NAME}\` exists: ${r.indexAfter ? '✓' : '✗'}`);
    L.push('');
  }
  if (r.errors.length) { L.push('## Errors'); L.push(''); r.errors.forEach((e) => L.push(`- ❌ ${e}`)); L.push(''); }
  L.push('## Final status');
  L.push('');
  L.push('```');
  L.push(r.status);
  L.push('```');
  L.push('');
  try { writeFileSync(REPORT, L.join('\n'), 'utf8'); console.log(`✓ wrote ${REPORT}`); }
  catch (e) { console.error(`✗ failed to write ${REPORT}: ${e.message}`); }
}

async function main() {
  try { assertNoOldEnv(); } catch (e) { console.error(`✗ ${e.message}`); process.exit(7); }

  const r = {
    ts: new Date().toISOString(),
    mode: dryRun ? 'dry-run' : 'apply',
    host: '(unknown)',
    fks: [], indexBefore: false, applied: false,
    fkAbsentAfter: null, indexAfter: null, errors: [], status: STATUS.FAILED,
  };

  if (doApply && env('ALLOW_REPAIR_YANDEX_SCHEMA') !== 'true') {
    r.errors.push('Real repair refused: ALLOW_REPAIR_YANDEX_SCHEMA != true.');
    writeReport(r);
    console.error('✗ --apply requires ALLOW_REPAIR_YANDEX_SCHEMA=true. Use --dry-run to plan.');
    process.exit(7);
  }

  let yaUrl;
  try { yaUrl = requireEnv('YANDEX_DATABASE_URL'); }
  catch (e) { r.errors.push(e.message); writeReport(r); console.error(`✗ ${e.message}`); process.exit(2); }
  r.host = maskHost(yaUrl);
  const ca = resolveCa();
  if (!ca.ok) { r.errors.push(`Yandex CA unavailable (${ca.reason})`); writeReport(r); console.error(`✗ Yandex CA unavailable: ${ca.reason}`); process.exit(2); }

  let client;
  try { client = await getYandexClient(yaUrl, ca.pem, { applicationName: 'prod-to-yandex-repair-audit-fk' }); }
  catch (e) { r.errors.push(`connection failed: ${e.message}`); writeReport(r); fatal(e); }

  try {
    console.log(`${tag('YA')} repair audit FK ${dryRun ? '(DRY-RUN — no changes)' : '(APPLY)'} host=${r.host}`);
    r.fks = (await client.query(FIND_FK_SQL)).rows.map((x) => x.conname).filter(isIdent);
    r.indexBefore = (await client.query(FIND_IDX_SQL, [INDEX_NAME])).rows.length > 0;
    console.log(`${tag('YA')} FK(s): ${r.fks.length ? r.fks.join(', ') : '(none)'} · index ${INDEX_NAME}: ${r.indexBefore ? 'present' : 'absent'}`);

    if (dryRun) {
      for (const f of r.fks) console.log(`${tag('YA')} PLANNED: ALTER TABLE public.boq_items_audit DROP CONSTRAINT ${f};`);
      console.log(`${tag('YA')} PLANNED: CREATE INDEX IF NOT EXISTS ${INDEX_NAME} ON public.boq_items_audit(boq_item_id);`);
      r.status = STATUS.DRY;
      writeReport(r);
      console.log(`${tag('YA')} status: ${STATUS.DRY}`);
      return;
    }

    // ---- APPLY (single transaction) ----
    await client.query('BEGIN');
    for (const f of r.fks) {
      await client.query(`ALTER TABLE public.boq_items_audit DROP CONSTRAINT "${f}"`);
      console.log(`${tag('YA')} dropped constraint ${f}`);
    }
    await client.query(`CREATE INDEX IF NOT EXISTS ${INDEX_NAME} ON public.boq_items_audit(boq_item_id)`);
    await client.query('COMMIT');
    r.applied = true;

    r.fkAbsentAfter = (await client.query(FIND_FK_SQL)).rows.length === 0;
    r.indexAfter = (await client.query(FIND_IDX_SQL, [INDEX_NAME])).rows.length > 0;
    if (r.fkAbsentAfter && r.indexAfter) {
      r.status = STATUS.OK;
      console.log(`${tag('YA')} ✓ FK absent, index present`);
    } else {
      r.status = STATUS.FAILED;
      r.errors.push(`post-apply check failed: fkAbsent=${r.fkAbsentAfter} indexExists=${r.indexAfter}`);
    }
    writeReport(r);
    console.log(`${tag('YA')} status: ${r.status}`);
    if (r.status === STATUS.FAILED) process.exit(1);
  } catch (e) {
    try { await client.query('ROLLBACK'); } catch { /* ignore */ }
    r.status = STATUS.FAILED;
    r.errors.push(String(e?.message || e).replace(/postgres(?:ql)?:\/\/\S+/gi, '<redacted-conn>'));
    writeReport(r);
    fatal(e);
  } finally {
    await client.end().catch(() => {});
  }
}

main().catch((e) => fatal(e));
